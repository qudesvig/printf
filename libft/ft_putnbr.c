#include "includes/libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putnbr(-21474);
			n = 83648;
		}
		else
		{
			ft_putchar('-');
			n *= -1;
		}
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
}
